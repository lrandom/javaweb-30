package bt;

public class Calculator {
    public float add(float a, float b) {
        return a + b;
    }

    public float sub(float a, float b) {
        return a - b;
    }

    public float multiple(float a,float b) {
        return a * b;
    }

    public float divide(float a,float b) {
        return a / b;
    }
}
